public class App {
    public static void main(String[] args) throws Exception {

                // OPERADOR TERNÁRIO
        // Usa-se operador condicional ternário que é a '?', para atribuir um valor a uma variável mediante uma determinada condição
        
        int v1 = 10;
        int v2 = 0;
        int r = v2 > 0 ? v1 / v2 : 0;
        System.out.format("Resultado: %d%n", r); 
        
    }
}
